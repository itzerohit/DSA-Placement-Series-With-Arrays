package Loops;
import java.util.Scanner;
public class ForLoop {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for (int i = 1; i<=n; i++){
            // int i = 1 declaration ;
            // i <= 8 condition
            // i++ increment
            System.out.println("Hello World");
        }
    }
}


