// WE NEED TO PRINT THE REVERSE

package Loops;
import java.util.Scanner;
public class HW4 {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for (int i = n; i >0; i--){
            System.out.println(i);
        }
    }
}
